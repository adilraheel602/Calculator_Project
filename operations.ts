const Sum = (num1:number, num2: number):number => {
    return num1 + num2;
};
const Subtraction = (num1:number, num2: number):number => {
    return num1 - num2;
};
const Multiply = (num1:number, num2: number):number => {
    return num1 * num2;
};
const Division = (num1:number, num2: number):number => {
    return num1 / num2;
};
export {Sum, Subtraction, Multiply, Division};